"""
常量模块统一导出。

为保持向后兼容性，从子模块导出所有常量。
原有 `from backend.src.constants import XXX` 的代码可正常工作。

注意：请勿在 `backend/src/` 根目录再创建 `constants.py`，否则会与本 package 同名产生歧义，
并容易导致“常量加错位置/导入失败”。

子模块划分：
- status: 状态码（RUN_STATUS_*, STEP_STATUS_*, STATUS_*, EVAL_STATUS_*）
- action_types: 行动类型（ACTION_TYPE_*, TASK_OUTPUT_TYPE_*）
- stream_tags: 流式标签（STREAM_TAG_*, SSE_TYPE_*）
- error_messages: 错误码和消息（HTTP_STATUS_*, ERROR_CODE_*, ERROR_MESSAGE_*）
- agent_config: Agent 配置（AGENT_*, DB_*, APP_*, DEFAULT_*）
- think_config: Think 模式配置（THINK_*, EXECUTOR_ROLE_*）
- llm_config: LLM 配置（LLM_*, CHAT_ROLE_*, DEFAULT_LLM_MODEL）
- misc_config: 杂项配置（SQL_*, CLEANUP_*, MEMORY_*, GRAPH_*, SKILL_VALIDATION_*, OP_*）
- prompts: Prompt 模板（*_PROMPT_TEMPLATE）
"""

